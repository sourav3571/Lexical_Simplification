        "                    'word': cand_lower,",
        "                    'zipf_frequency': cand_freq,",
        "                    'frequency_gain': cand_freq - orig_freq",
        "                })",
        "        ",
        "        # Sort candidates descending by Zipf frequency",
        "        filtered.sort(key=lambda x: x['zipf_frequency'], reverse=True)",
        "        return filtered",
