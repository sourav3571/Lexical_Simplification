        "# Cell 7 - Complex Word Identifier Class",
        "class ComplexWordIdentifier:",
        "    \"\"\"",
        "    Identifies which content words in the sentence are 'complex'.",
        "    Complexity is determined using 3 factors:",
        "    1. Zipf frequency < 4.0",
